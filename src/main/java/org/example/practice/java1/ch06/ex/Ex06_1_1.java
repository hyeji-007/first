package org.example.practice.java1.ch06.ex;

public class Ex06_1_1 {

    public static void main(String[] args) {
        simpleOpr(10, 2);
    }

    public static void simpleOpr(int n1, int n2) {
        System.out.println("n1 + n2 = " + (n1 + n2));
        System.out.println("n1 - n2 = " + (n1 - n2));
        System.out.println("n1 * n2 = " + (n1 * n2));
        System.out.println("n1 / n2 = " + (n1 / n2));
        System.out.println("n1 % n2 = " + (n1 % n2));
    }
}
